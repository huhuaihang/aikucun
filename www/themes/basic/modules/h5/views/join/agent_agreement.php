            <p class="MsoNormal" align="center" style="text-align:center;">
                <b>
                    <h2 style="text-align:left;">
                        2017年开放平台招商标准<span></span>
                    </h2>
            <p style="text-align:left;">
                <strong>第一章<span> 2017</span>年平台招商重点</strong>
            </p>
            <p style="text-align:left;">
                1.1企业<span>/</span>品牌<span></span>
            </p>
            <p style="text-align:left;">
                平台鼓励有实力的企业或著名品牌入驻，尊重品牌传统和内涵，欢迎优质品牌旗舰店入驻。<span></span>
            </p>
            <p style="text-align:left;">
                1.2商品<span></span>
            </p>
            <p style="text-align:left;">
                能够满足会员群体优质、有特色的商品。<span></span>
            </p>
            <p style="text-align:left;">
                根据类目结构细分的商品配置。类目规划详见<span><a href="https://help.jd.com/rule/ruleDetail.action?ruleId=2756" target="https://rule.jd.com/rule/_blank"><span>《</span></a><a href="https://rule.jd.com/rule/ruleDetail.action?ruleId=2756" target="https://rule.jd.com/rule/_blank">2017</a><a href="https://rule.jd.com/rule/ruleDetail.action?ruleId=2756" target="https://rule.jd.com/rule/_blank"><span>年<span>平台<span>类目一览表</span></span></span></a><a href="https://help.jd.com/rule/ruleDetail.action?ruleId=2756" target="https://rule.jd.com/rule/_blank"><span>》</span></a></span>。<span></span>
            </p>
            <p style="text-align:left;">
                1.3农商户<span></span>
            </p>
            <p style="text-align:left;">
                平台欢迎种植农商户<span>/</span>农业电商企业等入驻。<span></span>
            </p>
            <p style="text-align:left;">
                <a name="2"></a><strong>第二章适用范围</strong>
            </p>
            <p style="text-align:left;">
                本标准适用于平台所有卖家，个别特殊类目以类目招商标准为准。<span></span>
            </p>
            <p style="text-align:left;">
                &nbsp;
            </p>
            <p style="text-align:left;">
                <a name="3"></a><strong>第三章入驻须知</strong>
            </p>
            <p style="text-align:left;">
                3.1平台合作代理商招商入住的，入驻申请流程及相关的收费说明均以平台官方招商页面为准。<span></span>
            </p>
            <p style="text-align:left;">
                3.2申请与平台合作，应在线签署《平台商家入驻合作协议》。<a name="_GoBack"></a><span></span>
            </p>
            <p style="text-align:left;">
                3.2平台有权根据包括但不限于品牌需求、公司经营状况、服务水平等其他因素退回卖家入驻申请。<span></span>
            </p>
            <p style="text-align:left;">
                3.3平台有权在申请入驻及后续经营阶段要求卖家提供其他资质。<span></span>
            </p>
            <p style="text-align:left;">
                3.4平台将结合各行业发展动态、国家相关规定及消费者购买需求，不定期更新招商标准。<span></span>
            </p>
            <p style="text-align:left;">
                3.5卖家必须如实提供资料和信息：<span></span>
            </p>
            <p style="text-align:left;">
                3.5.1请务必确保申请入驻及后续经营阶段提供的相关资质和信息的真实性、完整性、有效性（若卖家提供的相关资质为第三方提供，包括但不限于商标注册证、授权书等，请务必先行核实文件的真实有效完整性），一旦发现虚假资质或信息的，平台将不再与卖家进行合作并有权根据平台规则及与卖家签署的相关协议之约定进行处理；<span></span>
            </p>
            <p style="text-align:left;">
                3.5.2卖家应如实提供其店铺运营的主体及相关信息，包括但不限于店铺实际经营主体、代理运营公司等信息；<span></span>
            </p>
            <p style="text-align:left;">
                3.5.3平台关于卖家信息和资料变更有相关规定的从其规定，但卖家如变更<span>3.5.2</span>项所列信息，应提前<span>10</span>日书面告知平台；如未提前告知平台，平台将根据平台规则进行处理。<span></span>
            </p>
            <p style="text-align:left;">
                3.6平台接受个人身份及企业身份（包含个体工商户）入驻，暂时不接受非中国大陆注册企业的入驻申请。<span></span>
            </p>
            <p style="text-align:left;">
                <a name="4"></a><strong>&nbsp;</strong>
            </p>
            <p style="text-align:left;">
                <strong>第四章店铺类型及相关要求</strong>
            </p>
            <p style="text-align:left;background:white;">
                4.1开设店铺类型及相关要求详见《平台各店铺类型入驻资质要求》。<span></span>
            </p>
            <p style="text-align:left;">
                4.2各类型店铺命名详细说明，请见<a href="https://help.jd.com/rule/ruleDetail.action?ruleId=2598" target="https://rule.jd.com/rule/_blank"><span>《</span></a>店铺类型及命名规范<span><a href="https://help.jd.com/rule/ruleDetail.action?ruleId=2598" target="https://rule.jd.com/rule/_blank"><span>》</span></a></span>。<span></span>
            </p>
            <p style="text-align:left;">
                &nbsp;
            </p>
            <p style="text-align:left;">
                <a name="5"></a><strong>第五章平台申请入驻资质标准</strong>
            </p>
            <p style="text-align:left;">
                5.1平台申请入驻资质标准详见《平台各店铺类型入驻资质要求》。
            </p>
            <p style="text-align:left;">
                <a name="6"></a><strong>第六章开店入驻限制</strong>
            </p>
            <p style="text-align:left;">
                6.1品牌入驻限制：<span></span>
            </p>
            <p style="text-align:left;">
                6.1.1与平台或本公司旗下等已有的品牌、频道、业务、类目等相同或相似名称的品牌；<span></span>
            </p>
            <p style="text-align:left;">
                6.1.2包含行业名称或通用名称的品牌；<span></span>
            </p>
            <p style="text-align:left;">
                6.1.3包含知名人士、地名的品牌；<span></span>
            </p>
            <p style="text-align:left;">
                6.1.4与知名品牌相同或近似的品牌。<span></span>
            </p>
            <p style="text-align:left;">
                6.2商号（或字号）入驻限制：<span></span>
            </p>
            <p style="text-align:left;">
                6.2.1与平台或本公司旗下的品牌、频道、业务、类目等相同或近似；<span></span>
            </p>
            <p style="text-align:left;">
                6.2.2与知名品牌相同或相近的。<span></span>
            </p>
            <p style="text-align:left;">
                6.3授权有关说明<span></span>
            </p>
            <p style="text-align:left;">
                6.3.1以商标持有人为源头的一级授权，是指商标权人授权卖家销售其品牌的商品；<span></span>
            </p>
            <p style="text-align:left;">
                6.3.2以商标持有人为源头的二级内完整授权，是指商标权人授权某公司销售其品牌商品，该公司授权卖家销售该品牌商品，卖家需提供商标权人给予某公司（个人）的授权书及某公司（个人）给卖家的授权书；<span></span>
            </p>
            <p style="text-align:left;">
                6.4经营类目限制，卖家开店所经营的类目应当符合平台的相关标准，详细请参考《<span>2017</span>年平台经营类目资费一览表》。<span></span>
            </p>
            <p style="text-align:left;">
                6.5同一主体入驻的店铺限制：<span></span>
            </p>
            <p style="text-align:left;">
                6.5.1商品重合度：要求店铺间同一类目经营的品牌及商品不得重复，经过审批的店铺不受此项约束。<span></span>
            </p>
            <p style="text-align:left;">
                6.6同一主体重新入驻平台限制：<span></span>
            </p>
            <p style="text-align:left;">
                6.6.1严重违规、资质造假被平台清退的，永久限制入驻；<span></span>
            </p>
            <p style="text-align:left;">
                6.6.2若卖家一自然年内主动退出<span>1</span>次，则自最后一次完成退出之日起<span>6</span>个月内限制入驻。<span></span>
            </p>
            <p style="text-align:left;">
                6.7续签限制：<span></span>
            </p>
            <p style="text-align:left;">
                6.7.1须在每年<span>12</span>月<span>20</span>日<span>00</span>时之前完成续签申请的提交，每年<span>12</span>月<span>31</span>日<span>18</span>时之前完成平台使用费的缴纳，如果上一年及下一年资费及资料未补足，平台将在每年<span>1</span>月<span>30</span>日<span>24</span>时终止店铺服务并清退出平台。<span></span>
            </p>
            <p style="text-align:left;">
                6.7.2卖家在服务期内未通过<span><a href="https://help.jd.com/rule/ruleDetail.action?ruleId=2752" target="https://rule.jd.com/rule/_blank"><span>《</span></a></span>平台商家店铺考核标准<span><a href="https://help.jd.com/rule/ruleDetail.action?ruleId=2752" target="https://rule.jd.com/rule/_blank"><span>》</span></a></span>，平台有权拒绝商家提出相应续展服务期的申请。<span></span>
            </p>
            <p style="text-align:left;">
                <strong>&nbsp;</strong>
            </p>
            <p style="text-align:left;">
                <strong>第七章平台保证金<span>/</span>平台使用费<span>/</span>费率标准</strong>
            </p>
            <p style="text-align:left;">
                7.1保证金：卖家以“一店铺一保证金“原则向平台缴纳的用以保证店铺规范运营及对商品和服务质量进行担保的金额。当卖家发生侵权、违约、违规行为时，平台可以依照与卖家签署的协议中相关约定及平台规则扣除相应金额的保证金作为违约金或给予消费者的赔偿。<span></span>
            </p>
            <p style="text-align:left;">
                7.1.1保证金的调整、补足、退还、扣除、赔偿等依据卖家签署的相关协议及平台规则约定办理；<span></span>
            </p>
            <p style="text-align:left;">
                7.1.2平台各经营类目对应的保证金标准详见《2017年度开放平台各类目资费一览表》。<span></span>
            </p>
            <p style="text-align:left;">
                7.1.3跨类目保证金：就高原则，保证金按最高金额的类目缴纳；经营过程中增加的类目对应的保证金与原有保证金不一致，商家须补交差额部分。<span></span>
            </p>
            <p style="text-align:left;">
                7.1.4保证金不足额时，卖家应在出现该等情况后<span>10</span>个自然日内及时补缴足额的保证金使可用余额达至最低保证金金额。平台可向卖家发出续费通知，如卖家在平台上有未结货款，平台有权从该等款项中扣除以补足保证金。如卖家逾期未能补足保证金，则平台有权对其店铺进行监管或终止服务。<span></span>
            </p>
            <p style="text-align:left;">
                7.2平台使用费：卖家依照与平台签署的相关协议使用平台各项服务时缴纳的固定技术服务费用。平台各经营类目对应的平台使用费标准详见<span><a href="https://help.jd.com/rule/ruleDetail.action?ruleId=2607" target="https://rule.jd.com/rule/_blank"><span>《</span></a></span>2017年度开放平台各类目资费一览表<a href="https://help.jd.com/rule/ruleDetail.action?ruleId=2607" target="https://rule.jd.com/rule/_blank"><span>》</span></a>。续签卖家的续展服务期间对应平台使用费须在每年<span>12</span>月<span>31</span>日<span>18</span>时前一次性缴纳；新签卖家须在申请入驻获得批准时一次性缴纳相应服务期间的平台使用费。<span></span>
            </p>
            <p style="text-align:left;">
                7.2.1平台使用费结算：<span></span>
            </p>
            <p style="text-align:left;">
                7.2.1.1平台使用费是入驻商户按年度缴纳，如卖家主动要求停止店铺服务的不返还平台使用费；<span></span>
            </p>
            <p style="text-align:left;">
                7.2.1.2卖家因违规行为或资质造假被清退的不返还平台使用费；<span></span>
            </p>
            <p style="text-align:left;">
                7.2.1.3每个店铺的平台使用费依据相应的服务期计算并缴纳。服务开通之日在每月的<span>1</span>日至<span>15</span>日（含）间的，开通当月按一个月收取平台使用费，服务开通之日在每月的<span>16</span>日（含）至月底最后一日间的，开通当月不收取平台使用费；<span></span>
            </p>
            <p style="text-align:left;">
                7.2.1.4拥有独立店铺<span>ID</span>的为一个店铺，若卖家根据经营情况须开通多个店铺的，须按照店铺数量缴纳相应的平台使用费。<span></span>
            </p>
            <p style="text-align:left;">
                7.3费率：卖家根据合作经营模式在平台达成交易时向平台缴纳的费用。平台各经营模式各经营类目对应的费率标准详见《平台商铺入驻合作协议》及《2017年度开放平台各类目资费一览表》。<span></span>
            </p>
            <p style="text-align:left;">
                &nbsp;
            </p>
            <p style="text-align:left;">
                <a name="8"></a><strong>第八章店铺服务期</strong>
            </p>
            <p style="text-align:left;">
                8.1卖家每个店铺的第一个服务期自服务开通之日起至最先到达的<span>12</span>月<span>31</span>日止，第二个服务期自<span>1</span>月<span>1</span>日起至次年<span>12</span>月<span>31</span>日止，第三个、第四个……服务期以此类推，以一年为周期，除非店铺或与卖家签署的相关协议提前终止。<span></span>
            </p>
            <p style="text-align:left;">
                8.2卖家每个店铺的服务开通之日以平台通知或系统记录的时间为准。<span></span>
            </p>
            <p style="text-align:left;">
                8.3卖家应在店铺每个服务期届满前<span>30</span>日向甲方提出续展的申请，缴纳续展服务期的平台使用费和提交其经营所需的全部有效资质，并经平台审核。<span></span>
            </p>
            <p style="text-align:left;">
                8.4卖家未提出续展申请或申请未通过平台审核的，自店铺服务期满之日起，平台将不再向卖家提供该店铺的任何服务。<span></span>
            </p>
            <p style="text-align:left;">
                &nbsp;
            </p>
            <p style="text-align:left;">
                <a name="9"></a><strong>第九章附则</strong>
            </p>
            <p style="text-align:left;">
                9.1本标准中的“日”如无特殊说明均指连续的自然日，如<span>10</span>日是连续<span>10</span>个自然日，一日按<span>24</span>小时计算。<span></span>
            </p>
            <p style="text-align:left;">
                9.2平台用户的行为，发生在本标准生效之日以前的，适用当时的标准；发生在本标准生效之日以后的，适用本标准。<span></span>
            </p>
            <p style="text-align:left;">
                9.3平台可能随时修改、补充或更新本规则，并在该规则生效前在平台给予以公告，上述公告一经发布即视为通知所有使用平台服务的主体，一经发起举报即为接受变更后的规则内容。<span></span>
            </p>
            <p style="text-align:left;">
                <strong>此标准于<span>2017</span>年<span>10</span>月<span>1</span>日发布，于<span>2017</span>年<span>10</span>月<span>10</span>日生效。</strong>
            </p>
